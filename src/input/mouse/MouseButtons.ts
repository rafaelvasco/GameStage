// MouseButtons.ts - Mouse button enumeration for input tracking

/**
 * Comprehensive mouse button enumeration with numeric values for optimal performance
 * Each button has a unique numeric identifier for ultra-fast operations
 */
export const enum MouseButtons {
  // Standard mouse buttons (0-2)
  Left = 0,
  Middle = 1,
  Right = 2,
  
  // Additional buttons (3-6)
  Back = 3,
  Forward = 4,
  Button5 = 5,
  Button6 = 6,
  
  // Total count for validation and array sizing
  TOTAL = 7
}